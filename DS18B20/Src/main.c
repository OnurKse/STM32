void Read_Rom(void)
{
	OneWireDevices = TM_OneWire_First(&OneWire);
	while (OneWireDevices)
	{
		HAL_Delay(100);
		TM_OneWire_GetFullROM(&OneWire, &address[TempSensorCount][0]);
		TempSensorCount++;
		OneWireDevices = TM_OneWire_Next(&OneWire);
	}
	sprintf(buffer, "\n\r%d sensors found", TempSensorCount);
	debug(buffer);
	for (uint8_t index = 0; index < TempSensorCount; index++)
	{
		debug("\n\rRom: ");
		for (i = 0; i < 8; i++) { // we need 144 bytes for memory
			sprintf(buffer, "%x ", address[index][i]);
			debug(buffer);
		}
	}
}

void Get_Temp(float *array)
{
	for (uint8_t index = 0; index < TempSensorCount; index++)
	{
		TM_OneWire_Reset(&OneWire);
		TM_OneWire_Select(&OneWire, address[index]);
		debug("\n\rRead Scratch: ");
		TM_OneWire_WriteByte(&OneWire, 0x44); // Read Scratch
		TM_OneWire_Reset(&OneWire);
		TM_OneWire_Select(&OneWire, address[index]);
		TM_OneWire_WriteByte(&OneWire, 0xBE); // Read Scratch
		for (i = 0; i < 9; i++) { // we need 9 bytes for memory
			temp[i] = TM_OneWire_ReadByte(&OneWire);
			sprintf(buffer, "%x ", temp[i]);
			debug(buffer);
		}
		uint8_t MSB = temp[1];
		uint8_t LSB = temp[0];

		float tempRead = ((MSB << 8) | LSB); //using two's compliment
		float TemperatureSum = tempRead / 16;
		array[index] = TemperatureSum;
	}
}
